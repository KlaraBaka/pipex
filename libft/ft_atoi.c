#include "libft.h"

int	ft_atoi(const char *str)
{
	unsigned int		i;
	long int			neg;
	long int			res;

	res = 0;
	neg = 1;
	i = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' || str[i] == '\f'
		|| str[i] == '\v' || str[i] == '\r')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i++] - '0';
		if (res > 2147483648 && neg == -1)
			return (0);
		if (res > 2147483648 && neg == 1)
			return (-1);
	}
	return ((int)(res * neg));
}
